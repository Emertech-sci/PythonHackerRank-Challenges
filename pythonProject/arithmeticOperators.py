print("Introduce dos numeros a operar")
a = int(input())
b = int(input())
suma = a + b
resta = a - b
producto = a * b
int_division = a // b
double_division = a / b

print(f"el resultado de {a} + {b} = {suma}")
print(f"el resultado de {a} - {b} = {resta}")
print(f"el resultado de {a} X {b} = {producto}")
print(f"el resultado de {a} // {b} = {int_division}")
print(f"el resultado de {a} / {b} = {double_division}")